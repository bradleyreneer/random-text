package org.reneer.verse;

/**
 * @author breneer
 *
 */

public class PassageText {
    private String title;
    private String copyWrite;
    private Integer index;
    private String text;
    /**
     * @return the title
     */
    public String getTitle() {
        return this.title;
    }
    /**
     * @param title the title to set
     */
    public void setTitle(String title) {
        this.title = title;
    }
    /**
     * @return the copyWrite
     */
    public String getCopyWrite() {
        return this.copyWrite;
    }
    /**
     * @param copyWrite the copyWrite to set
     */
    public void setCopyWrite(String copyWrite) {
        this.copyWrite = copyWrite;
    }
    /**
     * @return the index
     */
    public Integer getIndex() {
        return this.index;
    }
    /**
     * @param index the index to set
     */
    public void setIndex(Integer index) {
        this.index = index;
    }
    /**
     * @return the text
     */
    public String getText() {
        return this.text;
    }
    /**
     * @param text the text to set
     */
    public void setText(String text) {
        this.text = text;
    }
    
    

}
